console.log(students[0][0]) //Budi
console.log(students[0][1]) //Jakarta
console.log(students[3][1]) //3
console.log(students[3][0]) //0
console.log(students[1][0]) //Andi
console.log(students[1][1]) // Bandung
// itung dari baris dulu baru ke kolom
